-module(spinet_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, StartArgs) ->
	lager:info("Starting spinet, Args=~p", [StartArgs]),
    spinet_sup:start_link(StartArgs).

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

